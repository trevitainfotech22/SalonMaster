
package salon.master.GETSET;

public class product {

    String item;
    int Qty;

    public product(String item, int Qty) {
        this.item = item;
        this.Qty = Qty;
    }

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public int getQty() {
        return Qty;
    }

    public void setQty(int Qty) {
        this.Qty = Qty;
    }

    
  

   

}
